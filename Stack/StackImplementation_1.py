# Implementation of Stack using Python Lists
stack =[]

# Pushing element in stack
stack.append(1)
stack.append(2)
stack.append(3)

#Stack with Items
print(stack)

# pop() function to to remove element from the stack in LIFO order
print("Element poped from the stack")
print(stack.pop())
print(stack.pop())
print(stack.pop())


# Empty Stac
print(stack)



